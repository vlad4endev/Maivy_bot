export function getAboutStepCount(): number {
  return ABOUT_STEPS.length;
}

export function getAboutStepText(step: number): string {
  const item = ABOUT_STEPS[step - 1];
  if (!item) {
    return "Раздел не найден.";
  }

  return `<b>${item.title}</b>\n\n${item.body}`;
}

